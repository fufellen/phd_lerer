#include "VIBRr.h"



void VO2_cold(double x, double *eps1, double *eps2)
{// vozvraschaet interpolirov-e znacenie eps=eps1-ieps2
	int q=21; // kolichestvo ishodnih tocek
	double dx = 250.0, e1,e2;
	int i;

	double xi[21]; //massiv dlin voln
	double fs[21][2];//massiv compleksnyh n (n=n'-i*n'')
	
	xi[0]=1000.0;
	xi[1]=1250.0;
	xi[2]=1500.0;
	xi[3]=1750.0;
	xi[4]=2000.0;
	xi[5]=2250.0;
	xi[6]=2500.0;
	xi[7]=2750.0;
	xi[8]=3000.0;
	xi[9]=3250.0;
	xi[10]=3500.0;
	xi[11]=3750.0;
	xi[12]=4000.0;
	xi[13]=4250.0;
	xi[14]=4500.0;
	xi[15]=4750.0;
	xi[16]=5000.0;
	xi[17]=5250.0;
	xi[18]=5500.0;
	xi[19]=5750.0;
	xi[20]=6000.0;



	//n(n'-i*n'') (VO2)
	fs[0][0]=3.3;
	fs[1][0]=3.4;
	fs[2][0]=3.5;
	fs[3][0]=3.65;
	fs[4][0]=3.7;
	fs[5][0]=3.75;
	fs[6][0]=3.8;
	fs[7][0]=3.82;
	fs[8][0] = 3.82;
	fs[9][0] = 3.82;
	fs[10][0] = 3.82;
	fs[11][0] = 3.82;
	fs[12][0] = 3.82;
	fs[13][0] = 3.82;
	fs[14][0] = 3.82;
	fs[15][0] = 3.82;
	fs[16][0] = 3.82;
	fs[17][0] = 3.82;
	fs[18][0] = 3.82;
	fs[19][0] = 3.82;
	fs[20][0] = 3.82;	

	fs[0][1]=0.7;
	fs[1][1]=0.6;
	fs[2][1]=0.5;
	fs[3][1]=0.4;
	fs[4][1]=0.33;	
	fs[5][1]=0.3;
	fs[6][1]=0.26;
	fs[7][1]=0.24;
	fs[8][1]=0.22;
	fs[9][1]=0.2;
	fs[10][1]=0.18;
	fs[11][1]=0.16;
	fs[12][1]=0.14;	
	fs[13][1] = 0.12;
	fs[14][1] = 0.11;
	fs[15][1] = 0.10;
	fs[16][1] = 0.09;
	fs[17][1] = 0.08;
	fs[18][1] = 0.08;
	fs[19][1] = 0.08;
	fs[20][1] = 0.08;

	for (i = 0; i < q; i++)
	{
		if (fabs(x - xi[i]) < 1.0e-7)
		{
			e1 = fs[i][0];
			e2 = fs[i][1];
			break;
		}
		else
		{
			if (x > xi[i] && x < xi[i + 1])
			{			
				e1 = (fs[i + 1][0] * (x - xi[i]) - fs[i][0] * (x - xi[i + 1])) / dx;
				e2 = (fs[i + 1][1] * (x - xi[i]) - fs[i][1] * (x - xi[i + 1])) / dx;
				break;
			}
		}
	}//i
	
	//printf("\n  l=%6.3lf   n_1=%6.3lf    n_2=%6.3lf", x, e1, e2);
	*eps1 = e1 * e1 - e2 * e2;
	*eps2 = -2.0 * e1 * e2;
}

